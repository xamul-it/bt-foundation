def main() -> int:
    args = parse_args()
    matrix_path, resolved_reconcile_dir, matrix_source = resolve_matrix_path(args)
    out_json, out_html, out_log = resolve_default_outputs(args, matrix_path, resolved_reconcile_dir)
    logger = setup_logger(args.log_level, out_log)

    logger.info("Input matrix source: %s", matrix_source)
    logger.info("Input matrix path  : %s", matrix_path)
    if resolved_reconcile_dir is not None:
        logger.info("Resolved reconcile : %s", resolved_reconcile_dir)
    logger.info("Output JSON path   : %s", out_json)
    logger.info("Output HTML path   : %s", out_html)
    logger.info("Log file path      : %s", out_log)

    rows = load_rows(matrix_path)
    result = evaluate(
        rows,
        alpha=max(1e-9, float(args.alpha)),
        min_sample=max(1, int(args.min_sample)),
        permutations=max(100, int(args.permutations)),
        seed=int(args.seed),
        include_paired_debug=bool(args.include_paired_debug),
    )
    result["meta"]["matrix_path"] = str(matrix_path)
    result["meta"]["matrix_source"] = matrix_source
    result["meta"]["output_json"] = str(out_json)
    result["meta"]["output_html"] = str(out_html)
    result["meta"]["log_file"] = str(out_log)

    print_report(result)

    out_json.parent.mkdir(parents=True, exist_ok=True)
    out_json.write_text(json.dumps(result, indent=2, ensure_ascii=True), encoding="utf-8")
    print(f"\nJSON written: {out_json}")
    logger.info("JSON written: %s", out_json)

    out_html.parent.mkdir(parents=True, exist_ok=True)
    out_html.write_text(build_html(result), encoding="utf-8")
    print(f"HTML written: {out_html}")
    logger.info("HTML written: %s", out_html)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
